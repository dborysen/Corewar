#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	char	*p;
	int		i;

	p = (char *)b;
	i = 0;
	while (n)
	{
		p[i] = c;
		i++;
		n--;
	}
	return (p);
}
