#include "libft.h"

int		ft_one_equ(char *str1, char *str2)
{
	int		i;

	i = 0;
	if (!ft_strlen(str1))
		return (1);
	while (str1[i])
	{
		if (ft_strchr(str2, str1[i]))
			return (1);
		i++;
	}
	return (0);
}
