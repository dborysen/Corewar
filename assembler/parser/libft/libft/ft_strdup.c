#include <stdlib.h>

char			*ft_strdup(char *s1)
{
	int		l;
	int		i;
	char	*s2;

	i = 0;
	l = 0;
	while (s1[l])
		l++;
	s2 = (char*)malloc(sizeof(char) * (l + 1));
	if (s2 == 0)
		return (0);
	while (i < l)
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
