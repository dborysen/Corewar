#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int		i;
	int		o;
	int		r;
	char	*s1;

	i = 0;
	r = 0;
	s1 = (char*)(s);
	o = ft_strlen(s1) + 1;
	while (i < o)
	{
		if (s1[i] == c)
			r = i;
		i++;
	}
	if (s1[r] == c)
		return (s1 + r);
	return (0);
}
