#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int		i;
	char	*s1;

	i = 0;
	if (s && *s && c)
	{
		s1 = (char*)s;
		while (s1[i] != c && s1[i])
			i++;
		if (s1[i] == c)
			return (s1 + i);
	}
	return (0);
}
